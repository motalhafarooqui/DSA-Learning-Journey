#include <iostream>
using namespace std;

int main(){
    int n;
    cin >> n;
    cout << "Value of n : " << n << "\n";

    long int m;
    cin >> m;
    cout << "Value of m : " << m << "\n";

    long long int o;
    cin >> o;
    cout << "Value of o : " << o << "\n";

    float p;
    cin >> p;
    cout << "Value of p : " << p << "\n";

    double d;
    cin >> d;
    cout << "Value of d : " << d << "\n";

    string ch;
    cin >> ch;
    cout << ch << "\n";

    string str;
    getline(cin, str);
    cout << str;

    return 0;
}